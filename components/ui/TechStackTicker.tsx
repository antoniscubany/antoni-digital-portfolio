// Duplicate array for seamless infinite scroll
const duplicatedTech = [...techStack, ...techStack, ...techStack, ...techStack];

export default function TechStackTicker() {
    const [isPaused, setIsPaused] = useState(false);
    const [hoveredTech, setHoveredTech] = useState<TechItem | null>(null);
    const [tooltipPosition, setTooltipPosition] = useState({ x: 0, y: 0 });
    const containerRef = useRef<HTMLDivElement>(null);

    const handleMouseEnter = (tech: TechItem, e: React.MouseEvent) => {
        setIsPaused(true);
        setHoveredTech(tech);
        setTooltipPosition({ x: e.clientX, y: e.clientY });
    };

    const handleMouseMove = (e: React.MouseEvent) => {
        setTooltipPosition({ x: e.clientX, y: e.clientY });
    };

    const handleMouseLeave = () => {
        setIsPaused(false);
        setHoveredTech(null);
    };

    return (
        <div className="relative w-full overflow-hidden py-8">
            {/* Gradient overlays for fade effect */}
            <div className="absolute left-0 top-0 bottom-0 w-32 bg-gradient-to-r from-[#030303] to-transparent z-10 pointer-events-none" />
            <div className="absolute right-0 top-0 bottom-0 w-32 bg-gradient-to-l from-[#030303] to-transparent z-10 pointer-events-none" />

            {/* Ticker container */}
            <div
                ref={containerRef}
                className="flex items-center"
                style={{
                    animation: isPaused ? "none" : "ticker 30s linear infinite",
                }}
            >
                {duplicatedTech.map((tech, index) => (
                    <motion.div
                        key={`${tech.name}-${index}`}
                        className="flex items-center gap-3 px-8 cursor-pointer"
                        onMouseEnter={(e) => handleMouseEnter(tech, e)}
                        onMouseMove={handleMouseMove}
                        onMouseLeave={handleMouseLeave}
                        whileHover={{ scale: 1.1 }}
                        transition={{ duration: 0.2 }}
                    >
                        <div
                            className="transition-all duration-300"
                            style={{
                                color: hoveredTech?.name === tech.name ? tech.color : "#666",
                            }}
                        >
                            {tech.icon}
                        </div>
                        <span
                            className="text-sm font-medium whitespace-nowrap transition-colors duration-300"
                            style={{
                                color: hoveredTech?.name === tech.name ? tech.color : "#666",
                            }}
                        >
                            {tech.name}
                        </span>
                    </motion.div>
                ))}
            </div>

            {/* Tooltip */}
            <AnimatePresence>
                {hoveredTech && (
                    <motion.div
                        initial={{ opacity: 0, y: 10, scale: 0.9 }}
                        animate={{ opacity: 1, y: 0, scale: 1 }}
                        exit={{ opacity: 0, y: 10, scale: 0.9 }}
                        transition={{ duration: 0.2 }}
                        className="fixed z-50 pointer-events-none"
                        style={{
                            left: tooltipPosition.x,
                            top: tooltipPosition.y - 60,
                            transform: "translateX(-50%)",
                        }}
                    >
                        <div
                            className="px-4 py-2 rounded-lg text-sm font-medium"
                            style={{
                                background: "rgba(10, 10, 10, 0.9)",
                                backdropFilter: "blur(10px)",
                                border: `1px solid ${hoveredTech.color}40`,
                                boxShadow: `0 0 20px ${hoveredTech.color}20`,
                            }}
                        >
                            <span style={{ color: hoveredTech.color }}>
                                {hoveredTech.name}:
                            </span>{" "}
                            <span className="text-gray-300">{hoveredTech.description}</span>
                        </div>
                    </motion.div>
                )}
            </AnimatePresence>

            {/* CSS Animation */}
            <style jsx>{`
        @keyframes ticker {
          0% {
            transform: translateX(0);
          }
          100% {
            transform: translateX(-50%);
          }
        }
      `}</style>
        </div>
    );
}
